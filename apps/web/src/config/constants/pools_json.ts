export const platformNames = pools_json.reduce((acc, curr) => {
    if (curr.originPlatform && curr.platform_logo) {
        const exists = acc.some((item) => item.name === curr.originPlatform);
        if (!exists) {
            const count = pools_json.filter(
                (item) => item.originPlatform === curr.originPlatform && !item.isDeprecated
            ).length;
            acc.push({
                name: curr.originPlatform,
                logo: curr.platform_logo,
                count,
            });
        }
    }
    return acc;
}, [] as { name: string; logo: string; count: number }[]);

export const tokenImages = pools_json.reduce((acc, curr) => {
    if (curr.logo1) {
        const exists = acc.some((item) => item.name === curr.name1);
        if (!exists) {
            acc.push({
                name: curr.name1,
                logo: curr.logo1,
            });
        }
    }
    if (curr.logo2) {
        const exists2 = acc.some((item) => item.name === curr.name2);
        if (!exists2) {
            acc.push({
                name: curr.name2 || "",
                logo: curr.logo2 || "",
            });
        }
    }
    return acc;
}, [] as { name: string; logo: string }[]);

